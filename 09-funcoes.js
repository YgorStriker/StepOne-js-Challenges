// Crie as seguintes funções:
// 1- Função de soma, com o nome ‘plus', que retorna a soma de dois números.
function plus(n1, n2) {
    const add = n1 + n2;
    return add;
}
console.log(plus(27, 18));
console.log(plus(12, 43));

// 2- Função de subtração, com o nome ‘minus’, que retorna a subtração de dois números
function minus(n1, n2) {
    const sub = n1 - n2;
    return sub;
}
console.log(minus(33, 19));
console.log(minus(19, 41));

// 3- Função de multiplicação, com o nome ‘mult', que retorna a multiplicação de dois números
function mult(n1, n2) {
    const multply = n1 * n2;
    return multply;
}
console.log(mult(3, 17));
console.log(mult(14, 5));

// 4- Função de divisão, com o nome 'division', que retorna a divisão do primeiro número pelo outro
function division(n1, n2) {
    const divide = n1 / n2;
    return divide;
}
console.log(division(50, 2));
console.log(division(6, 8));

