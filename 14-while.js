// Utilizando o while, faça uma estrutura de repetição que dê o console dos números 100 a 1.
let u = 50;
let p = 2;
let d = 100;

while (u<=100) {
    console.log(u);
    u++;
};

while (p<=100) {
    console.log(p);
    p = p + 2;
};

while (d>=1) {
    console.log(d);
    d--;
};